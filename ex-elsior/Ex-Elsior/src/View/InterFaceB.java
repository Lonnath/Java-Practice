    private void ivaCalculatedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ivaCalculatedActionPerformed
        boolean numVer = true;
        for (int i = 0; i < this.cantityCharge.getText().length(); i++) {
            if ((this.cantityCharge.getText().charAt(i) < 48 || this.cantityCharge.getText().charAt(i) > 57)) {
                numVer = false;
            }
        }
        for (int i = 0; i < this.minimalCantity.getText().length(); i++) {
            if (this.minimalCantity.getText().charAt(i) < 48 || this.minimalCantity.getText().charAt(i) > 57) {
                numVer = false;
            }
        }
        for (int i = 0; i < this.priceBase.getText().length(); i++) {
            if (this.priceBase.getText().charAt(i) < 48 || this.priceBase.getText().charAt(i) > 57) {
                numVer = false;
            }
        }
        if (!numVer) {
            JOptionPane.showMessageDialog(this, "Campo Invalido (Numeros Obligatorio)");
        } else {
            if ((this.nameProductCharge.getText().equals("") || this.nameProductCharge.getText().length() == 0) || (this.cantityCharge.getText().equals("") || this.cantityCharge.getText().length() == 0) || (this.minimalCantity.getText().equals("") || this.minimalCantity.getText().length() == 0) || (this.priceBase.getText().equals("") || this.priceBase.getText().length() == 0) || (!this.paperShop.isSelected() && !this.drugShop.isSelected() && !this.superShop.isSelected())) {
                JOptionPane.showMessageDialog(null, "Campos Vacios, Verifique la Informaci√≥n.");

            } else {
                double iva = 0;
                if (this.superShop.isSelected()) {
                    iva = 0.04;
                }
                if (this.drugShop.isSelected()) {
                    iva = 0.12;
                }
                if (this.paperShop.isSelected()) {
                    iva = 0.16;
                }

                this.ivaText.setText(Integer.parseInt(this.priceBase.getText()) + (Integer.parseInt(this.priceBase.getText()) * iva) + "");
                JOptionPane.showMessageDialog(this, "IVA CALCULADO");
                this.chargeProduct.setEnabled(true);
            }
    }//GEN-LAST:event_ivaCalculatedActionPerformed
    }

    private void chargeProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chargeProductActionPerformed
        String selection = "";
        if (this.drugShop.isSelected()) {
            selection = "DROGUERIA";
        }
        if (this.superShop.isSelected()) {
            selection = "SUPERMERCADO";
        }
        if (this.paperShop.isSelected()) {
            selection = "PAPELERIA";
        }
        this.inventory.chargeItem(this.nameProductCharge.getText(), this.cantityCharge.getText(), this.minimalCantity.getText(), this.priceBase.getText(), selection);

        this.chargeProduct.setEnabled(false);
    }//GEN-LAST:event_chargeProductActionPerformed

